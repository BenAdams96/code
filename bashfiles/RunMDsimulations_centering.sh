for i in {001..856}
do
    echo %%%%%%%%%%%%%%%%%         NEXT MOLECULE         %%%%%%%%%%%%%%%%%%%%%%%%%%%
    echo MDsimulations_${DATASET}/$i
    if [ -d "MDsimulations_${DATASET}/$i" ]; then
        cd "MDsimulations_${DATASET}/$i"

        PREFIX=${i}_prod

        to center the ligand and get rid of periodic boundaries
        echo -e "2\n0" | gmx trjconv -s $PREFIX.tpr -f $PREFIX.xtc -o ${PREFIX}_center.xtc -center -pbc mol
        rm $PREFIX.xtc
        mv ${PREFIX}_center.xtc ${PREFIX}.xtc

        cd ../..
    else
        echo "Directory MDsimulations_${DATASET}/$i does not exist."
    fi
done
